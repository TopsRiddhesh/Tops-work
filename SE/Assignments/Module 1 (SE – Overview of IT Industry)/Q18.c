// How to make collaborators on Github?

/*Ans:
To add collaborators to a GitHub repository, follow these steps:

1. Navigate to the Repository: Go to the repository on GitHub where you want to add collaborators.

2. Repository Settings: Click on the "Settings" tab at the top right of the repository's page.

3. Manage Access: In the left sidebar, click on "Manage access". This will show you the current list of people who have access to the repository.

4. Invite Collaborators:

To invite a collaborator, start typing their GitHub username, full name, or email address into the search box under "Invite a collaborator".
    1. Select the appropriate user from the dropdown list.
    2. Choose the access level for the collaborator:
    3. Write access: Allows the collaborator to push to the repository (if they have write permissions).
    4. Read access: Allows the collaborator to pull/fetch from the repository but not push changes.

5. Send Invitation: Click on the "Add" button next to the collaborator's name to send them an invitation. GitHub will notify the user via email and also display a notification in their GitHub account.

6. Accept Invitation: The invited collaborator will receive an email with a link to accept the invitation. They can also go directly to the repository's page on GitHub, where they will see an invitation notification (bell icon at the top right). They need to click on this notification and accept the invitation.

7. Confirmation: Once the collaborator accepts the invitation, they will have the specified access rights to the repository. You can see their name added to the list of collaborators under "Manage access".
*/