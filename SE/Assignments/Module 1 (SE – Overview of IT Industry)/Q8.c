// What is HTTPs & URLs?

/*Ans :
1. HTTPs:

HTTPS stands for HyperText Transfer Protocol Secure. It is an extension of HTTP (HyperText Transfer Protocol) and is used for secure communication over a computer network, typically the internet.

2. URLs:

URL stands for Uniform Resource Locator. It is a reference or address used to locate resources (such as web pages, files, images, videos, etc.) on the internet.*/