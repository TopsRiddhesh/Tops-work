// What is the use of git add, git commit, git push & git pull?

/*Ans:
The commands git add, git commit, git push, and git pull are fundamental to working with Git, a distributed version control system. Here's a brief explanation of each:

git add: This command is used to stage changes for commit. When you modify files in your working directory, Git initially doesn't track these changes. git add adds these changes to the staging area, preparing them to be included in the next commit.

git commit: This command records the staged changes to the repository's history. Each commit captures a snapshot of the repository at a specific point in time, along with a commit message that describes the changes made.

git push: This command is used to upload local repository content to a remote repository. It sends the committed changes from your local repository to the remote repository, allowing others to see and access your work.

git pull: This command is used to fetch and download content from a remote repository and immediately update your local repository to match that content. It performs a git fetch followed by a git merge to bring the remote branch up to date with your local branch.
*/