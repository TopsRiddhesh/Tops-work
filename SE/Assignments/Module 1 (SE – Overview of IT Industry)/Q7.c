// What is Web Browser & Web Server?

/*Ans
1. Web Browser:
A web browser is a software application used to access and view content on the World Wide Web (WWW). It interprets and renders web pages written in languages such as HTML, CSS, and JavaScript.
2. Web Server:
A web server is a computer or software application that stores, processes, and delivers web pages to clients (such as web browsers) upon request over the internet. */