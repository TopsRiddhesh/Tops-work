// What is LAN, WAN & MAN?

/*Ans: 
LAN, WAN, and MAN are types of networks categorized based on their geographical scope and the number of devices they interconnect.Here's what each term refers to:
1. LAN (Local Area Network):

Definition: A LAN is a network that connects computers and devices within a limited geographical area, such as a single building, office, or campus.
2. WAN (Wide Area Network):

Definition: A WAN is a network that spans a large geographical area, connecting multiple LANs and other types of networks together across cities, countries, or continents.
3. MAN (Metropolitan Area Network):

Definition: A MAN is a network that spans a metropolitan area, typically a city or large campus, providing connectivity to a larger geographical area than a LAN but smaller than a WAN.*/